package com.phonefo.admin.domain;

public class ClientVO {
	private int num;
	private String path;
	
	


	public int getNum() {
		return num;
	}




	public void setNum(int num) {
		this.num = num;
	}




	public String getPath() {
		return path;
	}




	public void setPath(String path) {
		this.path = path;
	}




	@Override
	public String toString() {
		return "ClientVO [num=" + num + ", path=" + path + "]";
	}





	
	
	
	
	
	
	
	
	
	
	
	
	


	
	
	
}
