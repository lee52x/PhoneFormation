package com.phonefo.admin.domain;

public class SlideVO {
	private int state;
	private String path;
	
	
	
	
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	@Override
	public String toString() {
		return "SlideVO [state=" + state + ", path=" + path + "]";
	}
	
	
	

	
	
	
	
	
	
}
