package com.phonefo.admin.domain;

public class SlideVO {
	private int state;
	private String file;
	
	
	
	
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	
	public String getFile() {
		return file;
	}
	public void setFile(String file) {
		this.file = file;
	}
	@Override
	public String toString() {
		return "SlideVO [state=" + state + ", file=" + file + "]";
	}

	
	
	
	
	
	

	
	
	
	
	
	
}
